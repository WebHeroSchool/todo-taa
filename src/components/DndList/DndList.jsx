import {
  useState,
  useEffect,
  useRef,
} from 'react';

import {
  endOfGesture,
} from './handlersUtils';

import {
  onPointerMoveHandler,
  onPointerDownHandler,
  onPointerOverHandler,
  onTouchOverHandler,
  onPointerUpListener,
  onContextMenuHandler,
  onPointerLeaveHandler,
} from './handleEvents';

import TodoItemContainer from '../TodoItem/TodoItemContainer';


const DndList = ({
  list,
}) => {
  const [
    element,
    setElement,
  ] = useState(null);

  const [
    triggeredElement,
    setTriggeredElement,
  ] = useState(null);

  const ulElement = useRef(null);


  useEffect(() => {
    const onHtmlUpHandler = onPointerLeaveHandler.bind(
      event,
      ulElement,
      element,
      endOfGesture,
      setElement,
      setTriggeredElement,
    );


    document.documentElement.addEventListener('pointerup', onHtmlUpHandler);

    return () => {
      document.documentElement.removeEventListener('pointerup',
        onHtmlUpHandler);
    };
  }, [
    element,
  ]);


  return (
    <ul ref={ ulElement }
      onPointerDown={ event => onPointerDownHandler(
        event.target.closest('li'),
        ulElement,
        element,
        setElement,
      ) }
      onPointerMove={ event => onPointerMoveHandler(event, element) }
      onTouchMove={ event => onTouchOverHandler(
        event,
        element,
        ulElement,
        triggeredElement,
        setTriggeredElement,
      ) }
      onPointerOver={ event => onPointerOverHandler(
        event,
        ulElement,
        element,
        setTriggeredElement,
      ) }
      onPointerUp={ () => onPointerUpListener(
        element,
        triggeredElement,
        endOfGesture,
        setElement,
        setTriggeredElement,
      ) }
      onContextMenu={ onContextMenuHandler }
    >
      {
        list.map(
          item => (
            /* <li
              className={ styles.listItem }
              key={ element.value }
            >
              { element.value }
            </li> */
            <TodoItemContainer
              key={ item.id.toString() }
              // currentItem={ currentItem }
              // setCurrentItem={ setCurrentItem }
              // currentHeight={ currentHeight }
              // setCurrentHeight={ setCurrentHeight }
              { ...item }
            />
          )
        )
      }
    </ul>
  );
};

export default DndList;
